/**
 * Tagastab antud keele tГөlked. Kui vГөtit pole, proovitakse eesti keelt, siis key.
 */
export function getTranslations(locale) {
  const lang = locale && translations[locale] ? locale : DEFAULT_LOCALE;
  return translations[lang] || translations[DEFAULT_LOCALE] || {};
}

/**
 * t(key) вҖ“ tagastab praeguse keele stringi. Kasutada: t('common.untitled')
 */
export function createT(locale) {
  const tr = getTranslations(locale);
  const fallback = getTranslations(DEFAULT_LOCALE);
  return (key) => tr[key] ?? fallback[key] ?? key;
}
